/*
 * Step 5 firmware: run an MLIR-lowered LLVM-dialect kernel on TTNN tensor
 * data, entirely on the X280, with no PCIe round-trip during the modify
 * phase.
 *
 * The host hands us:
 *   - num_banks, num_pages, page_size, aligned_page_size
 *   - bank_x/bank_y for each active DRAM bank (shared by input and output)
 *   - input_bank_base[]  : base addr of the input  tensor in each bank
 *   - output_bank_base[] : base addr of the output tensor in each bank
 *
 * We:
 *   1. Configure one 128 GiB TLB window per bank (System Port, uncached) so
 *      that loads/stores in the window range translate to NOC unicast
 *      transactions to the bank's DRAM core. Same scheme as step 4.
 *   2. Stage the input tensor into a contiguous local-DRAM buffer
 *      (input_buf) by walking pages in logical order. For ROW_MAJOR layout
 *      this concatenates pages into the original row-major tensor.
 *   3. Call cpu_hoisted_ttir_abs_c4841e58, an externally-linked LLVM-dialect
 *      kernel (linked from poc/fw/cpu.o, generated by tt-mlir's CPU-hoisting
 *      pipeline). It takes two unrolled rank-1 memref descriptors:
 *         (alloc_ptr, aligned_ptr, offset, size, stride)  for input,
 *         (alloc_ptr, aligned_ptr, offset, size, stride)  for output.
 *      The kernel does element-wise abs over 4000 floats.
 *   4. Copy the output staging buffer back out to the TTNN output tensor's
 *      DRAM banks via the same TLB windows.
 *
 * Synchronization: kick at offset 0, done at the tail of Step5Task — same
 * "host writes body, then publishes kick separately; X280 ACQUIRE-loads
 * kick, RELEASE-stores done" pattern as step 2/4.
 */
#include <stdint.h>

#define MAX_BANKS 8
#define KERNEL_ELEMS 4000 /* must match the size baked into cpu.o */

struct Step5Task {
  volatile uint32_t kick; /* host -> fw */
  uint32_t num_banks;
  uint32_t aligned_page_size;
  uint32_t page_size;
  uint64_t num_pages;
  uint64_t input_bank_base[MAX_BANKS];
  uint64_t output_bank_base[MAX_BANKS];
  uint32_t bank_x[MAX_BANKS];
  uint32_t bank_y[MAX_BANKS];
  uint32_t done; /* fw -> host */
  uint32_t pad_done;
};

#define TASK_ADDR 0x400030100000ULL
#define KICK 0x1u
#define DONE 0xC0FFEE03u

/* X280 NOC TLB control registers (see tt-bh-linux/docs/addressing.md). */
#define TLB_2M_CONFIG_BASE 0x2FF00000U
#define TLB_2M_COUNT 224U
#define TLB_128G_CONFIG_STRIDE 0x0CU /* 3 x uint32 */
#define TLB_128G_CONFIG_BASE                                                   \
  (TLB_2M_CONFIG_BASE + 0x10U * TLB_2M_COUNT) /* = 0x2FF00E00 */

#define SYSTEM_PORT_BASE 0x30000000ULL
#define WINDOW_128G_BASE (SYSTEM_PORT_BASE + 0x80400000000ULL)
#define WINDOW_128G_SHIFT 37
#define WINDOW_128G_SIZE (1ULL << WINDOW_128G_SHIFT)

/*
 * memcpy. cpu.o (poc/fw/cpu.o) lowers llvm.memcpy.p0.p0.i64 to an
 * R_RISCV_CALL_PLT relocation against `memcpy`; we satisfy it ourselves so
 * the firmware remains freestanding (no libc).
 *
 * Byte-by-byte is fine here because cpu.o only calls memcpy on local
 * cached DRAM (its alloca -> output_buf copy). For our own NOC <-> local
 * staging copies we use noc_word_copy below — sub-word accesses through
 * the System-Port TLB window appear to hang on this silicon (step 4 only
 * ever uses 4-byte FP loads/stores against the same window).
 */
void *memcpy(void *dest, const void *src, uint64_t count) {
  uint8_t *d = (uint8_t *)dest;
  const uint8_t *s = (const uint8_t *)src;
  while (count--) {
    *d++ = *s++;
  }
  return dest;
}

/*
 * 4-byte-at-a-time copy, used for staging across the System-Port NOC TLB
 * window. Both pointers must be 4-byte aligned and `bytes` must be a
 * multiple of 4 — guaranteed for our use because page_size is verified to
 * be a multiple of sizeof(float), the staging buffers are aligned(64),
 * and the DRAM allocator gives us aligned bank base offsets.
 *
 * Why inline asm: vector-width (vl2r.v / vs2r.v, 128B/pass on this X280)
 * transactions through the System-Port NOC TLB window deliver one 32-float
 * chunk wrong per page on this silicon — step 4 only issues scalar 4-byte
 * ops against the same window and works fine, so the safe pattern is
 * "scalar lw/sw forever". A plain `d[i] = s[i]` C loop is *not* enough:
 * clang's loop-idiom recogniser spots the pattern, rewrites it to a call
 * to `memcpy`, and inlines the auto-vectorised memcpy back in place. The
 * `noinline` and `optimize("no-tree-vectorize")` attributes don't stop
 * idiom recognition. Embedding the load + store as a single inline-asm
 * block with a memory clobber gives the compiler nothing to recognise.
 */
static void noc_word_copy(uintptr_t dst, uintptr_t src, uint64_t bytes) {
  uint64_t words = bytes / 4;
  for (uint64_t i = 0; i < words; i++) {
    uint32_t tmp;
    __asm__ volatile("lw %0, 0(%1)\n\tsw %0, 0(%2)"
                     : "=&r"(tmp)
                     : "r"(src + i * 4), "r"(dst + i * 4)
                     : "memory");
  }
}

/*
 * The MLIR-lowered kernel from poc/fw/cpu.o. Its name is hash-derived from
 * the source MLIR; if you regenerate cpu.o from a different ttir source,
 * this symbol will change and the extern declaration must be updated to
 * match.
 *
 * The argument list is two unrolled rank-1 memref descriptors:
 *   (alloc_ptr, aligned_ptr, offset, size, stride)
 * for input followed by output. See MLIR's "Default Lowering of Memref"
 * documentation for the per-rank layout rules. For a 1-D contiguous buffer
 * we set alloc == aligned, offset = 0, size = N, stride = 1.
 */
extern void cpu_hoisted_ttir_abs_c4841e58(void *in_alloc, void *in_aligned,
                                          int64_t in_offset, int64_t in_size,
                                          int64_t in_stride, void *out_alloc,
                                          void *out_aligned, int64_t out_offset,
                                          int64_t out_size, int64_t out_stride);

static inline void configure_128g_window(uint32_t window_idx, uint32_t noc_x,
                                         uint32_t noc_y) {
  /* Same 96-bit (3 x uint32) unicast encoding as step4. */
  volatile uint32_t *cfg =
      (volatile uint32_t *)(uintptr_t)(TLB_128G_CONFIG_BASE +
                                       window_idx * TLB_128G_CONFIG_STRIDE);
  cfg[0] = 0;
  cfg[1] = (noc_x & 0x3Fu) | ((noc_y & 0x3Fu) << 6);
  cfg[2] = 0;
}

__attribute__((noreturn)) void fw_main(void) {
  struct Step5Task *task = (struct Step5Task *)TASK_ADDR;

  while (__atomic_load_n(&task->kick, __ATOMIC_ACQUIRE) != KICK) {
    __asm__ volatile("nop");
  }

  uint32_t num_banks = task->num_banks;
  uint32_t aligned_page_size = task->aligned_page_size;
  uint32_t page_size = task->page_size;
  uint64_t num_pages = task->num_pages;

  /* One TLB window per bank — shared by input and output since they live in
   * the same DRAM cores; only the per-bank base offset differs. */
  for (uint32_t b = 0; b < num_banks; b++) {
    configure_128g_window(b, task->bank_x[b], task->bank_y[b]);
  }
  __asm__ volatile("fence rw, rw" ::: "memory");

  /* Stack-resident staging buffers. fw.ld places _stack_top at DRAM_BASE +
   * 256 MiB so 4000-float buffers (~16 KiB each) are trivial. */
  __attribute__((aligned(64))) float input_buf[KERNEL_ELEMS];
  __attribute__((aligned(64))) float output_buf[KERNEL_ELEMS];

  /* Copy input tensor: bank-interleaved Tensix DRAM -> contiguous local. */
  for (uint64_t p = 0; p < num_pages; p++) {
    uint32_t bank_id = (uint32_t)(p % num_banks);
    uint64_t page_in_bank = p / num_banks;
    uint64_t addr_in_bank =
        task->input_bank_base[bank_id] + page_in_bank * aligned_page_size;
    uint64_t window_addr =
        WINDOW_128G_BASE + (uint64_t)bank_id * WINDOW_128G_SIZE + addr_in_bank;
    uintptr_t dst = (uintptr_t)input_buf + p * page_size;
    noc_word_copy(dst, (uintptr_t)window_addr, page_size);
  }

  /* Run the MLIR-lowered kernel on the contiguous staging buffer. */
  cpu_hoisted_ttir_abs_c4841e58(input_buf, input_buf,
                                /*offset=*/0,
                                /*size=*/(int64_t)KERNEL_ELEMS,
                                /*stride=*/1, output_buf, output_buf, 0,
                                (int64_t)KERNEL_ELEMS, 1);

  /* Copy result back: contiguous local -> bank-interleaved Tensix DRAM. */
  for (uint64_t p = 0; p < num_pages; p++) {
    uint32_t bank_id = (uint32_t)(p % num_banks);
    uint64_t page_in_bank = p / num_banks;
    uint64_t addr_in_bank =
        task->output_bank_base[bank_id] + page_in_bank * aligned_page_size;
    uint64_t window_addr =
        WINDOW_128G_BASE + (uint64_t)bank_id * WINDOW_128G_SIZE + addr_in_bank;
    uintptr_t src = (uintptr_t)output_buf + p * page_size;
    noc_word_copy((uintptr_t)window_addr, src, page_size);
  }

  __asm__ volatile("fence rw, rw" ::: "memory");
  __atomic_store_n(&task->done, DONE, __ATOMIC_RELEASE);

  for (;;) {
    __asm__ volatile("wfi");
  }
}
